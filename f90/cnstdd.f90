SUBROUTINE cnstdd
     
    !     THIS SUBROUTINE DEFINES COMMONLY USED PHYSICAL CONSTANTS
 
    !     THE /CONDAD/ COMMON BLOCK CONTAINS COMMONLY USED PHYSICAL
    !     CONSTANTS IN DOUBLE PRECISION FORM.
 
    !     THE /CONDAS/ COMMON BLOCK CONTAINS COMMONLY USED PHYSICAL
    !     CONSTANTS IN SINGLE PRECISION FORM.
 
    !     THE FOLLOWING IS A PARTIAL LIST OF ROUTINES THAT USE CONSTANTS
    !     FROM ONE OF THESE COMMON BLOCKS
    !              CDETM    ,CEAD1A   ,CONE     ,DETM5    ,DS1A     ,
    !              DTRIA    ,FRRD1A   ,GKAM     ,GP1      ,INPUT    ,
    !              KTUBE    ,MCONE    ,MFREE    ,MTUBE    ,PLA32    ,
    !              PLA42    ,PRESAX   ,QDMEM    ,RAND3    ,RAND8    ,
    !              REIG     ,RFORCE   ,ROD      ,SCONE1   ,SDR2C    ,
    !              STUBE1   ,TR1A     ,VDRB
 
    !     TYPE DECLARATION
 
    DOUBLE PRECISION :: dpi      ,d2pi     ,raddeg   ,degrad   , d4pisq
 
    !     COMMON STATEMENTS
 
    COMMON /condas/    pi       ,twopi    ,radeg    ,degra    , s4pisq
    COMMON /condad/    dpi      ,d2pi     ,raddeg   ,degrad   , d4pisq
 
 
    !     PI IS THE SINGLE PRECISION VALUE OF PI
 
    pi     =  3.141592653589793238462643D0
 
    !     DPI IS THE DOUBLE PRECISION VALUE OF PI
 
    dpi    =  3.141592653589793238462643D0
 
    !     TWOPI IS THE SINGLE PRECISION VALUE OF 2*PI
 
    twopi  =  6.283185307179586476925287D0
 
    !     D2PI IS THE DOUBLE PRECISION VALUE OF 2*PI
 
    d2pi   =  6.283185307179586476925287D0
 
    !     RADEG IS THE SINGLE PRECISION CONVERSION FACTOR FROM RADIANS TO
    !     DEGREES
 
    radeg  = 57.295779513082320876798155D0
 
    !     RADDEG IS THE DOUBLE PRECISION CONVERSION FACTOR FROM RADIANS TO
    !     DEGREES
 
    raddeg = 57.295779513082320876798155D0
 
    !     DEGRA IS THE SINGLE PRECISIONCONVERSION FACTOR FROM DEGREES TO
    !     RADIANS
 
    degra  =  0.017453292519943295769237D0
 
    !     DEGRAD IS THE DOUBLE PRECISION CONVERSION FACTOR FROM DEGREES TO
    !     RADIANS
 
    degrad =  0.017453292519943295769237D0
 
 
    !     S4PISQ IS THE SINGLE PRECISION VALUE OF 4*PI**2
 
    s4pisq = 39.478417604357434475337964D0
 
    !     D4PISQ IS THE DOUBLE PRECISION VALUE OF 4*PI**2
 
    d4pisq = 39.478417604357434475337964D0
 
    RETURN
END SUBROUTINE cnstdd
